package commandpattern;

public class Move {
	public void down() 
    { 
        System.out.println("move down"); 
    } 
    public void up() 
    { 
        System.out.println("move up"); 
    }
    public void right() 
    { 
        System.out.println("move right"); 
    } 
    public void left() 
    { 
        System.out.println("move left"); 
    } 
    public void stop() 
    { 
        System.out.println("move stop"); 
    } 
}
